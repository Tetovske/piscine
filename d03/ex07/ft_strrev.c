char	*ft_strrev(char *str)
{
	int i = 0;
    
    while (str[i] != '\0') ++i;
    
    int j = i - 1;
    int s = i / 2;
    int p = 0;
    
    while (s-- > 0)
	{
		char v = str[j];
		str[j--] = str[p];
		str[p++] = v;
	}	
}
