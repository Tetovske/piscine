int		ft_atoi(char *str)
{
	int		i;
    int		n;
	int		ret;
	int 	d;
	int 	rev;
	int 	s;

    i = 0;
    while (str[i] != '\0') i++;
    ret = 0;
    d = 1;
    rev = 0;
    s = i - 1;
    if (str[0] == '-')
	{
		rev = 1;
		d = -1;
	}
    while (s >= rev)
	{
		ret = ret + ((str[s--] - '0') * d);
		d = d * 10;
	}
    return (ret);	
}
