#include "ft_header.h"

void	prog_name(char *p_name)
{
	extern char *g_prog_name;
	int			i;

	i = -1;
	while (p_name[++i] == '.' || p_name[i] == '/')
		;
	g_prog_name = &p_name[i];
}
